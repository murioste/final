           <h2>About Donnelly Library</h2>
           <p>
             New Mexico Highlands University is a public comprehensive university serving our local and global communities. Our mission is to provide opportunities for undergraduate and graduate students to attain an exceptional education by fostering creativity, critical thinking and research in the liberal arts, sciences, and professions within a diverse community.
           </p>
           <p>
             The New Mexico Highlands University Library is committed to providing quality service and an excellent collection of materials to support the mission, goals, educational, and research needs of students, staff, faculty, administration, and community users and in support of the College's diverse curriculum. The Library is open to all Highlands University college students and staff; borrowing privileges are described in the Borrowing Privileges Policy.
           </p>
           <p>
             The Higher Learning Commission accredits approximately 1,000 colleges and universities that have a home base in one of 19 states that stretch from West Virginia to Arizona. HLC is a private, nonprofit regional accrediting agency.
           </p>
           <p>
             HLC’s mission is to assure and advance the quality of higher learning. It is recognized by the U.S. Department of Education and the Council for Higher Education Accreditation. The institutions HLC accredits include public, private not-for-profit, and proprietary institutions at degree levels from the associate’s through the doctoral degree level and from small, specialized institutions through large research universities.
           </p>

